#!/bin/bash

echo "Enter any two numbers: "
read num1  num2
echo "The sum of the given numbers is $(( "$num1" + "$num2" ))"
echo "The difference of the given numbers is $(( "$num1" - "$num2" ))"
echo "The product of the given numbers is $(( "$num1" * "$num2" ))"
echo "The quotient of the given numbers is $(( "$num1" / "$num2" ))"






